
import math as m  # Alias 'math' as 'm'

# 1. m.sqrt(x) - Square root of x
print(f"Square root of 16: {m.sqrt(16)}")

# 2. m.pow(x, y) - x raised to the power of y
print(f"2 raised to the power of 3: {m.pow(2, 3)}")

# 3. m.ceil(x) - Ceiling value of x
print(f"Ceiling of 4.3: {m.ceil(4.3)}")

# 4. m.floor(x) - Floor value of x
print(f"Floor of 4.3: {m.floor(4.3)}")

# 5. m.factorial(x) - Factorial of x
print(f"Factorial of 5: {m.factorial(5)}")


