# Example: len() function with a list
my_list = [10, 20, 30, 40]
# len() returns the number of items in the list
list_length = len(my_list)
print("Length of the list:", list_length)  # Output: Length of the list: 4

# Example: len() with a string
my_string = "Python"
# len() returns the number of characters in the string
string_length = len(my_string)
print("Length of the string:", string_length)  # Output: Length of the string: 6





# Example: type() with an integer
number = 42
print("Type of number:", type(number))  # Output: Type of number: <class 'int'>

# Example: type() with a list
my_list = [1, 2, 3]
print("Type of my_list:", type(my_list))  # Output: Type of my_list: <class 'list'>





# Example: max() and min() with a list
numbers = [5, 10, 15, 20, 25]
print("Maximum value:", max(numbers))  # Output: Maximum value: 25
print("Minimum value:", min(numbers))  # Output: Minimum value: 5





# Example: abs() with a positive and negative number
positive_num = 10
negative_num = -10
print("Absolute of 10:", abs(positive_num))   # Output: Absolute of 10: 10
print("Absolute of -10:", abs(negative_num))  # Output: Absolute of -10: 10



# Example: sum() with a list
numbers = [1, 2, 3, 4, 5]
print("Sum of numbers:", sum(numbers))  # Output: Sum of numbers: 15



# Example: input() to get user input
name = input("Enter your name: ")
print("Hello, " + name)  # Output depends on the input (e.g., if input is 'John', output will be 'Hello, John')



