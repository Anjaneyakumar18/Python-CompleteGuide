
# Logical Operators in Python

# Define two boolean variables
a = True
b = False

# Logical AND
logical_and = a and b
print("Logical AND (a and b):", logical_and)  # Output: False

# Logical OR
logical_or = a or b
print("Logical OR (a or b):", logical_or)    # Output: True

# Logical NOT
logical_not_a = not a
logical_not_b = not b
print("Logical NOT (not a):", logical_not_a)  # Output: False
print("Logical NOT (not b):", logical_not_b)  # Output: True
