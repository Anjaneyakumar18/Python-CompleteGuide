
# Assignment Operations in Python

# Initial value
x = 10
print("Initial value of x:", x)  # Output: 10

# Add and assign
x += 5
print("After x += 5:", x)  # Output: 15

# Subtract and assign
x -= 3
print("After x -= 3:", x)  # Output: 12

# Multiply and assign
x *= 2
print("After x *= 2:", x)  # Output: 24

# Divide and assign
x /= 4
print("After x /= 4:", x)  # Output: 6.0

# Floor divide and assign
x //= 2
print("After x //= 2:", x)  # Output: 3.0

# Modulus and assign
x %= 2
print("After x %= 2:", x)  # Output: 1.0

# Exponentiate and assign
x **= 3
print("After x **= 3:", x)  # Output: 1.0

# Reset x for bitwise operations
x = 5  # In binary: 0101

# Bitwise AND and assign
x &= 3  # 3 in binary: 0011
print("After x &= 3:", x)  # Output: 1 (in binary: 0001)

# Bitwise OR and assign
x |= 2  # 2 in binary: 0010
print("After x |= 2:", x)  # Output: 3 (in binary: 0011)

# Bitwise XOR and assign
x ^= 2  # 2 in binary: 0010
print("After x ^= 2:", x)  # Output: 1 (in binary: 0001)

# Left shift and assign
x <<= 1
print("After x <<= 1:", x)  # Output: 2 (in binary: 0010)

# Right shift and assign
x >>= 1
print("After x >>= 1:", x)  # Output: 1 (in binary: 0001)
